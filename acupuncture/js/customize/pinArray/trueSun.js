/**
 *
 * =================真太阳时==================
 */
function trueSunFun(tagName, tagCarrier) {
    //方法基础参数
    appendTag(tagName, tagCarrier, trueSunTagOne);
    trueSunTagOne = true;

    //北京时间
    let date = new Date();
    let beiJinTime = date.getFullYear() + "-" + (date.getMonth() + 1) + "-" + date.getDate() + nbsp + date.getHours() + ":" + date.getMinutes() + ":" + date.getSeconds()

    //平太阳时
    date.setMinutes(date.getMinutes() - 34)
    date.setSeconds(date.getSeconds() - 32)
    let ping = date.getFullYear() + "-" + (date.getMonth() + 1) + "-" + date.getDate() + nbsp + date.getHours() + ":" + date.getMinutes() + ":" + date.getSeconds()

    //真太阳时
    let zhenDate = trueSunUtil();
    let lvLiangZhen = zhenDate.getFullYear() + "-" + (zhenDate.getMonth() + 1) + "-" + zhenDate.getDate() + nbsp + zhenDate.getHours() + ":" + zhenDate.getMinutes() + ":" + zhenDate.getSeconds()

    $("#" + tagCarrier).html(
        "北京时间：" + beiJinTime
        + "<br>平太阳时：" + ping
        + "<br>真太阳时：<text style='font-weight: bolder;'>" + lvLiangZhen + "</text> - 吕梁"
        + "<br><a href='https://m.k366.com/cm/pp/taiyang.asp'>真太阳时计算器</a> | "
        + "<a href='http://mfsm.kvov.com/mfsmh.php'>公历出生日时间差</a> | "
        + "<a href='http://mfsm.kvov.com/mfsmhelp.php'>经纬度时差</a>"
    )

}

/**
 * 真太阳时工具类
 * @returns {Date}
 */
function trueSunUtil() {
    let date = new Date();
    for (let i = 0; i < ADJetLag.length; i++) {
        if (ADJetLag[i][0] === (date.getMonth() + 1).toString() && ADJetLag[i][1] === date.getDate().toString()) {
            //吕梁平太阳时
            date.setMinutes(date.getMinutes() - 34)
            date.setSeconds(date.getSeconds() - 32)
            if (ADJetLag[i][2].concat("+")) {
                date.setMinutes(date.getMinutes() + parseInt(ADJetLag[i][2].replace('+', '')));
                date.setSeconds(date.getSeconds() + parseInt(ADJetLag[i][3]));
            } else if (ADJetLag[i][2].concat("-")) {
                date.setMinutes(date.getMinutes() - parseInt(ADJetLag[i][2].replace('-', '')));
                date.setSeconds(date.getSeconds() - parseInt(ADJetLag[i][3]));
            }
        }
    }
    return date;
}